#include <stdio.h>

/**
 * add - add
 * @a: a
 * @b: b
 *
 * Return: int
 */
	int add(int a, int b)
	{
		return (a + b);
	}
/**
 * sub - su
 * @a: a
 * @b: b
 * Return: int
 */
int sub(int a, int b)
	{
		return (a - b);
	}
/**
 * mul - multiply
 * mod - module
 * @a: a
 * @b: b
 * Return: int
 */
int mul(int a, int b)
	{
		return (a * b);
	}
/**
 * div - module
 * @a: a
 * @b: b
 * Return: int
 */
int div(int a, int b)
	{
		return (a / b);
	}
/**
 * mod - module
 * @a: a
 * @b: b
 * Return: int
 */
int mod(int a, int b)
	{
		return (a % b);
	}
